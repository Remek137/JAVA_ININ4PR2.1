package pl.gda.wsb;

public class Human {

    String firstName;
    String lastName;
    String position;
    int age;
    boolean isWorking;

    public Human(String firstName, String lastName, String position, int age, boolean isWorking) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.position = position;
        this.age = age;
        this.isWorking = isWorking;
    }
}
